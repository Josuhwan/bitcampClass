/*논리형 데이터 타입에는 딱한개의 데이터타입이 있음
 * <boolean>
 * boolean의 값은 true와 false가 있다.
 * 
 * boolean자체도 우리가 해당 데이터타입의 변수를 직접 만들어서 쓰기
 * 보다는 우리가 나중에 배우게 될 연산자 라는 것을 사용하여 그 연산의 
 * 결과값을 그대로 쓰거나 메소드의 결과값을 그대로 쓰는 경우가 더 많다.
 */
public class Ex09Bloolean {
    public static void main(String[] args) {
        //boolaen myBool선언
        boolean myBool;
        //myBoolean 에 true를 할당
        myBool = true;
        //myBoolean출력
        System.out.println(myBool);
        //mybool에 false를 할당
        myBool = false;
        System.out.println(myBool);
        //
    }
}
